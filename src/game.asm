DEF playerX EQU $C000

SECTION "Game", ROM0

CreateGame:
    ; Reset scroll
    ld a, 0
    ld [rSCX], a

    call LoadTileMap
    call LoadSprites

    ld a, 32
    ld [playerX], a

    call PlaceSprites
    ret

UpdateGame:
    call PlaceSprites ; Update visuals with positions

    ; Check for dpad input
    ld hl, rP1
    ld a, P1F_5 ; using dpad mode
    ld [hl], a

    ld a, [hl] ; delay few cycles
    ld a, [hl] ; delay few cycles
    cpl ; invert
    and $0F 

    bit 0, a
    call nz, PlayerMoveRight

    bit 1, a
    call nz, PlayerMoveLeft

    ret

PlayerMoveRight:
    push af
    ld a, [playerX]
    inc a
    ld [playerX], a
    pop af
    ret

PlayerMoveLeft:
    push af
    ld a, [playerX]
    dec a
    ld [playerX], a
    pop af
    ret

LoadSprites:
    ; Loads tiles for player sprites
    ld a, $00
    ld [$FE02], a
    ld a, $01
    ld [$FE06], a
    ld a, $10
    ld [$FE0A], a
    ld a, $11
    ld [$FE0E], a
    ld a, $20
    ld [$FE12], a
    ld a, $21
    ld [$FE16], a

    ret

PlaceSprites:
    ; Positions player sprites X & Y
    ld a, 120 ; Y
    ld [$FE00], a
    ld [$FE04], a
    add 8
    ld [$FE08], a
    ld [$FE0C], a
    add 8
    ld [$FE10], a
    ld [$FE14], a

    ld a, [playerX] ; X
    ld [$FE01], a
    ld [$FE09], a
    ld [$FE11], a
    add 8
    ld [$FE05], a
    ld [$FE0D], a
    ld [$FE15], a
    ret

LoadTileMap:
    ; Load map
    ld hl, $9800
    ld de, GameTileMap
    ld bc, GameTileMapEnd - GameTileMap
.CopyTileMap
    ld a, [de]
    ld [hli], a
    inc de
    dec bc
    ld a, b
    or a, c
    jr nz, .CopyTileMap

    ret

