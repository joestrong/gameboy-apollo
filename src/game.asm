    ld a, 0
    ld [rSCX], a
    ; Load map
    ld hl, $9800
    ld de, GameTileMap
    ld bc, GameTileMapEnd - GameTileMap
CopyTileMap:
    ld a, [de]
    ld [hli], a
    inc de
    dec bc
    ld a, b
    or a, c
    jr nz, CopyTileMap

    ret

